#include "libft.h"

int	ft_atoi(char *str)
{
	int	i;
	int	num;

	i = 0;
	num = 1;

	if (!(*str))
	{
		return (0);
	}
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			num *= 10;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		i *= 10;
		i += (*str - 48);
		str++;
	}
	return (i * num);
}
